    private void registrar_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_ButtonActionPerformed
        String descricao;
        float saldo = 0.0f;
        

        descricao = textDescricao.getText();
        
        if( !textValor.getText().equals("") )
            saldo = Float.parseFloat( textValor.getText() );

        if ( !descricao.equals("") && !textValor.getText().equals("") )
            if( principal.controlador.buscarRenda(usuario, descricao ) != null )
                JOptionPane.showMessageDialog(null, "Renda j√° existe !", "Erro", JOptionPane.ERROR_MESSAGE);

            else {
                principal.controlador.registrarRenda( usuario, new Renda( descricao, saldo  ) );
                principal.controlador.armazenarUsuarios();
                JOptionPane.showMessageDialog(null, "Renda registrada com sucesso !", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
            }

        else
            JOptionPane.showMessageDialog(null, "Preencha Todos os campos !", "Erro", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_registrar_ButtonActionPerformed

