    private void registrar_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_ButtonActionPerformed
       String nome, numConta, decricao="";
       float saldo = 0.0f;
       Conta conta;
       
       
       nome = textNomeTitular.getText();
       numConta = textNumConta.getText();
       
       if( !textSaldo.getText().equals("") )
        saldo = Float.parseFloat( textSaldo.getText() );
       
       if ( !nome.equals("") && !numConta.equals("") && !textSaldo.getText().equals("") )
           if( principal.controlador.buscarConta( usuario, numConta ) != null )
                   JOptionPane.showMessageDialog(null, "Conta j√° existe !", "Erro", JOptionPane.ERROR_MESSAGE);
       
           else {
               principal.controlador.registrarConta( usuario, new Conta( nome, numConta, saldo,decricao ) );
               principal.controlador.armazenarUsuarios();
               JOptionPane.showMessageDialog(null, "Conta registrada com sucesso !", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
               this.dispose();
           }
       
       else
            JOptionPane.showMessageDialog(null, "Preencha Todos os campos !", "Erro", JOptionPane.ERROR_MESSAGE);
       
       
               
    }//GEN-LAST:event_registrar_ButtonActionPerformed

